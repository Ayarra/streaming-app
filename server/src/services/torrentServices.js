const extensions = [".avi", ".mkv", ".mp4", ".webm"];

exports.stream = async (req, res) => {
  console.log(req.params);
  const magnetLink = `magnet:?xt=urn:btih:${req.params.hash}`;
  const magnetLinkTrackers = `${magnetLink}${trackerString}`;
  const engine = torrentStream(magnetLinkTrackers);

  engine.on("error", (err) => {
    console.error("Torrent stream error:", err);
    res.status(500).send("Error streaming video");
  });

  engine.on("ready", () => {
    engine.files.forEach(function (file) {
      console.log("filename:", file.name);
    });
    const file = engine.files.find((file) => {
      return extensions.some((extension) => file.name.endsWith(extension));
    });

    if (!file) {
      res.status(404).send("File not found in torrent");
      return;
    }

    const stream = file.createReadStream();

    res.writeHead(200, {
      "Content-Type": "video/mp4",
    });
    stream.pipe(res);
  });
};
